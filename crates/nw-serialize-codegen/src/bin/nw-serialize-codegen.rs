fn main() -> Result<()> {
    let cli = Cli::parse();
    match cli.command {
        Command::Summary(args) => summary(&args),
        Command::Generate(args) => generate(&args),
        Command::NetworkSchema(args) => network_schema(&args),
        Command::NetworkRust(args) => network_rust(&args),
    }
}

fn summary(args: &CatalogArgs) -> Result<()> {
    let context = CodegenContext::from_jobs(args.jobs).context("build codegen worker pool")?;
    let compile_unit = context.install(|context| compile_catalog(args, context))?;
    let catalog_summary = compile_unit.catalog.summary();
    let field_summary = compile_unit.field_owner_evidence.summary();
    let (errors, warnings) = diagnostic_counts(&compile_unit);

    print_catalog_summary(catalog_summary);
    println!("field owner type IDs: {}", field_summary.owner_type_count);
    println!("field owner fields: {}", field_summary.field_count);
    println!("field owner exact keys: {}", field_summary.exact_key_count);
    println!(
        "field owner ambiguous exact keys: {}",
        field_summary.ambiguous_exact_key_count
    );
    println!("diagnostics: {errors} error(s), {warnings} warning(s)");
    Ok(())
}

fn generate(args: &GenerateArgs) -> Result<()> {
    let context =
        CodegenContext::from_jobs(args.catalog.jobs).context("build codegen worker pool")?;
    context.install(|context| generate_with_context(args, context))
}

fn generate_with_context(args: &GenerateArgs, context: &CodegenContext) -> Result<()> {
    let compile_unit = compile_catalog(&args.catalog, context)?;
    reject_compile_errors(&compile_unit)?;
    let root_selection = codegen_root_selection(&compile_unit.codegen_unit, args)?;
    let selected = root_selection.select_unit(&compile_unit.codegen_unit);
    let completed = completed_codegen_units(selected, compile_unit.codegen_unit.clone());

    match args.language {
        Language::All => {
            let tasks = [
                LanguageTask {
                    language: Language::Rust,
                    out: args.out.join("rust"),
                },
                LanguageTask {
                    language: Language::Go,
                    out: args.out.join("go"),
                },
                LanguageTask {
                    language: Language::TypeScript,
                    out: args.out.join("typescript"),
                },
            ];
            context.runner().try_map(&tasks, |task| {
                generate_language(&completed, args, task.language, &task.out, context)
            })?;
        }
        language => generate_language(&completed, args, language, &args.out, context)?,
    }

    let (errors, warnings) = diagnostic_counts(&compile_unit);
    println!("diagnostics: {errors} error(s), {warnings} warning(s)");
    Ok(())
}

fn network_schema(args: &NetworkSchemaArgs) -> Result<()> {
    let report = load_json_root(&args.ghidra_report, "Ghidra static network report")?;
    let mut schema = NetworkSchema::from_ghidra_static_network_report(&report)
        .context("normalize Ghidra static report into network schema")?;
    if let Some(source) = &args.typeregistry_source
        && let Some(ghidra_source) = schema.sources.first_mut()
    {
        ghidra_source.path = Some(source.clone());
    }
    let context =
        CodegenContext::from_jobs(args.catalog.jobs).context("build codegen worker pool")?;
    let compile_unit = context.install(|context| compile_catalog(&args.catalog, context))?;
    reject_compile_errors(&compile_unit)?;
    let serialize_merge = schema.merge_serialize_codegen_unit(
        &compile_unit.codegen_unit,
        Some(args.catalog.serialize_context.as_ref().map_or_else(
            || "embedded:resources/serialize.json".to_owned(),
            |path| path.display().to_string(),
        )),
    );
    println!(
        "serialize: {} source type(s), {} matched ({} by type id, {} by name, {} ambiguous name), {} filled name(s)",
        serialize_merge.source_type_count,
        serialize_merge.matched_type_count,
        serialize_merge.type_id_matched_count,
        serialize_merge.name_matched_count,
        serialize_merge.ambiguous_name_match_count,
        serialize_merge.filled_name_count
    );
    if let Some(typeindex_path) = &args.typeindex {
        let typeindex = load_json_root(typeindex_path, "typeindex JSON")?;
        let merge = schema
            .merge_typeindex_root(
                &typeindex,
                Some(
                    args.typeindex_source
                        .clone()
                        .unwrap_or_else(|| typeindex_path.display().to_string()),
                ),
            )
            .with_context(|| format!("merge typeindex from {}", typeindex_path.display()))?;
        println!(
            "typeindex: {} source type(s), {} matched, {} filled, {} conflict(s)",
            merge.source_type_count,
            merge.matched_type_count,
            merge.filled_type_index_count,
            merge.conflicting_type_index_count
        );
    }
    for signatures_path in &args.message_signatures {
        let signatures = load_message_signatures(signatures_path)?;
        let source = args
            .message_signatures_source
            .clone()
            .unwrap_or_else(|| signatures_path.display().to_string());
        let merge = schema.merge_message_signatures(&signatures, Some(source));
        println!(
            "message signatures: {} source message(s), {} matched, {} field name(s) filled, {} native type(s) filled, {} wire shape(s) filled, {} field-count mismatch, {} ambiguous, {} unmatched",
            merge.source_message_count,
            merge.matched_message_count,
            merge.field_name_filled_count,
            merge.native_type_filled_count,
            merge.wire_shape_filled_count,
            merge.field_count_mismatch_count,
            merge.ambiguous_message_count,
            merge.unmatched_message_count
        );
    }
    if let Some(root) = &args.message_rust_source_root {
        let inventory = RustSourceInventory::from_root(root, &context)
            .with_context(|| format!("scan Rust message source root {}", root.display()))?;
        let signatures = message_signatures_from_rust_source(&schema, &inventory);
        let source = args
            .message_rust_source
            .clone()
            .unwrap_or_else(|| root.display().to_string());
        let merge = schema.merge_message_signatures(&signatures, Some(source));
        println!(
            "message source: {} source message(s), {} matched, {} field name(s) filled, {} native type(s) filled, {} wire shape(s) filled, {} field-count mismatch, {} ambiguous, {} unmatched",
            merge.source_message_count,
            merge.matched_message_count,
            merge.field_name_filled_count,
            merge.native_type_filled_count,
            merge.wire_shape_filled_count,
            merge.field_count_mismatch_count,
            merge.ambiguous_message_count,
            merge.unmatched_message_count
        );
    }
    for overrides_path in &args.field_overrides {
        let overrides = load_field_overrides(overrides_path)?;
        let source = args
            .field_overrides_source
            .clone()
            .unwrap_or_else(|| overrides_path.display().to_string());
        let merge = schema.merge_field_overrides(&overrides, Some(source));
        println!(
            "field overrides: {} source field(s), {} matched, {} unmatched type(s), {} ambiguous type(s), {} unmatched field(s), {} ambiguous field(s), {} native type update(s), {} Rust type update(s), {} wire shape update(s), {} confidence update(s)",
            merge.source_field_count,
            merge.matched_field_count,
            merge.unmatched_type_count,
            merge.ambiguous_type_count,
            merge.unmatched_field_count,
            merge.ambiguous_field_count,
            merge.native_type_updated_count,
            merge.rust_type_updated_count,
            merge.wire_shape_updated_count,
            merge.confidence_updated_count
        );
    }
    let mut source = serde_json::to_vec_pretty(&schema).context("serialize network schema JSON")?;
    source.push(b'\n');
    if let Some(parent) = args
        .out
        .parent()
        .filter(|parent| !parent.as_os_str().is_empty())
    {
        fs::create_dir_all(parent)
            .with_context(|| format!("create output directory {}", parent.display()))?;
    }
    let changed = write_file_if_changed(&args.out, &source)?;
    println!(
        "network schema: {} type(s), {} register-field function(s), {} register-field call(s)",
        schema.summary.type_count,
        schema.summary.register_field_function_count,
        schema.summary.register_field_count
    );
    if changed {
        println!("wrote {}", args.out.display());
    } else {
        println!("unchanged {}", args.out.display());
    }
    Ok(())
}

fn network_rust(args: &NetworkRustArgs) -> Result<()> {
    let root = load_json_root(&args.schema, "network schema JSON")?;
    let schema = serde_json::from_value::<NetworkSchema>(root)
        .with_context(|| format!("parse network schema from {}", args.schema.display()))?;
    let source_audit = match &args.rust_source_root {
        Some(root) => {
            let inventory = RustSourceInventory::from_root(root, &CodegenContext::inline())
                .with_context(|| format!("scan Rust source root {}", root.display()))?;
            Some(audit_network_rust_source(&schema, &inventory))
        }
        None => None,
    };
    let output = NetworkRustEmitter::emit_descriptors(&schema)
        .context("emit network Rust descriptor source")?;
    write_path_if_changed(&args.out, output.source.as_bytes())?;
    if let Some(report_path) = &args.report {
        let mut report =
            serde_json::to_vec_pretty(&output.report).context("serialize network Rust report")?;
        report.push(b'\n');
        write_path_if_changed(report_path, &report)?;
    }
    if let Some(audit) = &source_audit {
        if let Some(report_path) = &args.rust_source_audit {
            let mut report =
                serde_json::to_vec_pretty(audit).context("serialize network Rust source audit")?;
            report.push(b'\n');
            write_path_if_changed(report_path, &report)?;
        }
        println!(
            "source audit: {} checked, {} current, {} missing, {} ambiguous, {} field mismatch, {} derive mismatch",
            audit.checked_type_count,
            audit.current_type_count,
            audit.missing_type_count,
            audit.ambiguous_type_count,
            audit.field_count_mismatch_count,
            audit.derive_mismatch_count
        );
    }
    println!(
        "network rust: {} descriptor(s), {} field descriptor(s), {} unnamed descriptor(s)",
        output.report.descriptor_count,
        output.report.field_descriptor_count,
        output.report.unnamed_descriptor_count
    );
    Ok(())
}

fn load_message_signatures(path: &Path) -> Result<Vec<NetworkMessageSignature>> {
    let root = load_json_root(path, "message signatures JSON")?;
    if root.is_array() {
        return serde_json::from_value(root)
            .with_context(|| format!("parse message signatures from {}", path.display()));
    }
    if let Some(messages) = root.get("messages") {
        return serde_json::from_value(messages.clone())
            .with_context(|| format!("parse message signatures from {}", path.display()));
    }
    bail!(
        "message signatures JSON {} must be an array or an object with `messages`",
        path.display()
    )
}

fn load_field_overrides(path: &Path) -> Result<NetworkFieldOverrideFile> {
    let root = load_json_root(path, "field overrides JSON")?;
    serde_json::from_value(root)
        .with_context(|| format!("parse field overrides from {}", path.display()))
}

fn message_signatures_from_rust_source(
    schema: &NetworkSchema,
    inventory: &RustSourceInventory,
) -> Vec<NetworkMessageSignature> {
    schema
        .types
        .iter()
        .filter(|network_type| should_audit_network_source_type(network_type))
        .filter_map(|network_type| {
            let schema_name = network_type.name.as_deref()?;
            let rust_name = rust_source_candidate_name(schema_name);
            let candidates =
                rust_source_candidates(inventory, network_type, schema_name, &rust_name);
            let [candidate] = candidates.as_slice() else {
                return None;
            };
            Some(message_signature_from_rust_source_item(
                network_type,
                schema_name,
                &candidate.item.name,
                candidate,
            ))
        })
        .collect()
}

fn message_signature_from_rust_source_item(
    network_type: &NetworkType,
    schema_name: &str,
    rust_name: &str,
    candidate: &RustSourceInventoryItem,
) -> NetworkMessageSignature {
    let fields = candidate
        .item
        .fields_in_order()
        .into_iter()
        .enumerate()
        .map(|(index, field)| NetworkMessageFieldSignature {
            index: u32::try_from(index).ok(),
            name: field.name.clone(),
            rust_type: Some(field.rust_type.clone()),
            native_type: None,
            wire_shape: rust_source_field_wire_shape(field),
        })
        .collect();

    NetworkMessageSignature {
        type_id: network_type.type_id,
        type_index: network_type.type_index,
        name: Some(schema_name.to_owned()),
        rust_name: Some(rust_name.to_owned()),
        source: None,
        fields,
    }
}

fn rust_source_field_wire_shape(field: &RustSourceField) -> Option<NetworkWireShape> {
    if let Some(marshal_as) = field.marshal_as.as_deref() {
        let compact = compact_rust_type(marshal_as);
        if compact.ends_with("HalfF32") {
            return Some(NetworkWireShape::HalfF32);
        }
        if compact.ends_with("VlqU32") {
            return Some(NetworkWireShape::VlqU32);
        }
        if compact.ends_with("VlqU64") {
            return Some(NetworkWireShape::VlqU64);
        }
        if compact.ends_with("QuatCompNorm") {
            return Some(NetworkWireShape::QuatCompNorm);
        }
    }

    match compact_rust_type(&field.rust_type).rsplit("::").next()? {
        "bool" => Some(NetworkWireShape::Bool),
        "u8" => Some(NetworkWireShape::U8),
        "u16" => Some(NetworkWireShape::U16),
        "u32" => Some(NetworkWireShape::U32),
        "u64" => Some(NetworkWireShape::U64),
        "f32" => Some(NetworkWireShape::F32),
        "f64" => Some(NetworkWireShape::F64),
        "Vec2" => Some(NetworkWireShape::Vec2),
        "Vec3" => Some(NetworkWireShape::Vec3),
        "Vec4" => Some(NetworkWireShape::Vec4),
        "Quat" => Some(NetworkWireShape::Quat),
        "Mat3" => Some(NetworkWireShape::Mat3),
        "Affine3A" => Some(NetworkWireShape::Affine3),
        "Aabb2d" => Some(NetworkWireShape::Aabb2d),
        "Aabb3d" => Some(NetworkWireShape::Aabb3d),
        "EntityRef" => Some(NetworkWireShape::EntityRef),
        "SequenceNumber" => Some(NetworkWireShape::SequenceNumber),
        "String" => Some(NetworkWireShape::String),
        "QuatCompNorm" => Some(NetworkWireShape::QuatCompNorm),
        _ => None,
    }
}

fn compact_rust_type(value: &str) -> String {
    value.chars().filter(|c| !c.is_whitespace()).collect()
}

#[derive(Debug, Clone, Serialize)]
